import type { GetServerSideProps, NextPage } from 'next'
import Head from 'next/head'
import { Card } from '../components/card/Card'
import {
    Fellow,
    getFellows,
    getPosts,
    Post,
} from '../domain/contentful/service'
import styles from '../styles/Home.module.css'

interface HomeProps {
    fellows: Fellow[]
    posts: Post[]
}

const Home: NextPage<HomeProps> = ({ fellows, posts }) => {
    return (
        <div>
            <Head>
                <title>Create Next App</title>
                <meta
                    name="description"
                    content="Generated by create next app"
                />
                <link rel="icon" href="/favicon.ico" />
            </Head>

            <main className="bg-white">
                <div className="grid grid-cols-4">
                    <>
                        {posts.map((post) => (
                            <Card
                                title={post.title}
                                text={post.description.content.toString()}
                                image={post.image?.fields.file.url}
                                key={post.title}
                            />
                        ))}
                        {fellows.map((fellow) => (
                            <Card
                                title={fellow.name}
                                text={fellow.description}
                                image={fellow.image?.fields.file.url}
                                key={fellow.name}
                            />
                        ))}
                    </>
                </div>
            </main>

            <footer className={styles.footer}></footer>
        </div>
    )
}

export const getServerSideProps: GetServerSideProps<HomeProps> = async () => {
    return {
        props: {
            fellows: await getFellows(),
            posts: await getPosts(),
        },
    }
}

export default Home
